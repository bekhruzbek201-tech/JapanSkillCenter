document.addEventListener("DOMContentLoaded", () => {
  const switchTarget = (lang) => {
    for (const [selector, texts] of Object.entries(i18n)) {
      document.querySelectorAll(selector).forEach(el => {
        if (texts[lang]) {
          el.innerHTML = texts[lang];
        }
      });
    }
  };

  const navBtns = document.querySelectorAll('.nav-right .lang-btn');
  const mobBtns = document.querySelectorAll('.mobile-lang-switcher .lang-btn');

  const allLangBtns = [...navBtns, ...mobBtns];
  
  allLangBtns.forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.preventDefault();
      const lang = btn.textContent.toLowerCase().trim();
      
      // Update UI active state
      allLangBtns.forEach(b => b.classList.remove('active'));
      const matchingBtns = document.querySelectorAll(`.lang-btn`);
      matchingBtns.forEach(b => {
        if (b.textContent.toLowerCase().trim() === lang) {
          b.classList.add('active');
        }
      });
      
      // Translate
      switchTarget(lang);
    });
  });
});
